# üìå –ö–∞–∂–¥—É—é –ø–∞—Ä—É —Å–æ—Ö—Ä–∞–Ω—è–π—Ç–µ —Å –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏.
import json


def convert_to_json(in_file, out_file):
    data = {}
    with open(in_file, 'r', encoding='utf-8') as f:
        for line in f:
            # strip —É–¥–∞–ª—è–µ—Ç –ø—Ä–æ–±–µ–ª—ã –≤ –Ω–∞—á–∞–ª–µ –∏ –≤ –∫–æ–Ω—Ü–µ
            name, number = line.strip().split(' | ')
# –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç –≤ —Å–ª–æ–≤–∞—Ä–µ data. –ö–ª—é—á - –∏–º—è, –∞ –∑–Ω–∞—á–µ–Ω–∏–µ - —á–∏—Å–ª–æ
            data[name.strip().capitalize()] = float(number)

    with open(out_file, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=4, ensure_ascii=False)

convert_to_json('result.txt', 'res.json')