def data_check(inp_data: str):
    day, month, year = inp_data.split('.')
    day_check = {
        '01': 31,
        '02': 29 if _leap_year(int(year)) else 28,
        '03': 31,
        '04': 30,
        '05': 31,
        '06': 30,
        '07': 31,
        '08': 31,
        '09': 30,
        '10': 31,
        '11': 30,
        '12': 31
    }

    if 0 < int(year) < 10000 and 0 < int(month) < 13 and 0 < int(day) <= day_check[month]:
        return True
    return False



